package com.example.charts;

public class SlideYChartPoint {

    public SlideYChartPoint(float x, float y) {
        this.x = x;
        this.y = y;
    }

    private float x;
    private float y;

    public void setX(float x) {
        this.x = x;
    }

    public float getX() {
        return x;
    }

    public void setY(float y) {
        this.y = y;
    }

    public float getY() {
        return y;
    }
}
